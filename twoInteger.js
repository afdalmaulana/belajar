const angka2 = 90;
console.log (`Nilai A = ${angka1}, Nilai B = ${angka2}`);
if (angka1 == angka2 ){
    console.log ("Nilai Angka Sama");
}else if(angka1 <= angka2){
    console.log (`Nilai ${angka2} Lebih Besar dari nilai ${angka1}`);
}else if(angka1 >= angka2){
    console.log (`Nilai ${angka1} Lebih Besar dari nilai ${angka2}`);
}else{
    console.log ("Invalid");
}